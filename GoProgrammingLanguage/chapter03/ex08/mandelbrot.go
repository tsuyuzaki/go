/**
 * 高倍率の水準でフラクタルをレンダリングするには高い算術精度が求められます。
 * complex64, complex128, big.Float, big.Rat の四つの異なる数値の表現を使って同じフラクタルを実装しなさい。
 * (最後の二つの型は math/big パッケージにあります。Float は任意精度ですが有界精度の浮動小数点を使っています。
 *  Rat は非有界精度の有理数を使っています。) 性能とメモリ使用量に関してどのような比較結果になりますか。
 *  どの倍率の水準になるとレンダリングの結果が視覚的に分かるようになりますか。
 */
package main

import (
	"image"
	"image/color"
	"image/png"
	"math"
	"math/cmplx"
	"os"
)

const (
	xmin, ymin, xmax, ymax = -2, -2, +2, +2
	width, height          = 1024, 1024
)

func main() {
	img := image.NewRGBA(image.Rect(0, 0, width, height))
	for py := 0; py < height; py++ {
		y := float64(py)/height*(ymax-ymin) + ymin
		for px := 0; px < width; px++ {
			x := float64(px)/width*(xmax-xmin) + xmin
			z := complex(x, y)
			img.Set(px, py, mandelbrot(z))
		}
	}
	png.Encode(os.Stdout, img)
}

func mandelbrot(z complex128) color.Color {
	const iterations = 200
	const contrast = 15

	var v complex128
	for n := uint8(0); n < iterations; n++ {
		v = v*v + z
		if cmplx.Abs(v) > 2 {
			px := ((real(z) - xmin) * width / (xmax - xmin))
			r := uint8(px / 4)
			g := uint8(math.MaxUint8 * cmplx.Abs(v) / math.Hypot(xmax, ymax))
			b := uint8(math.MaxUint8 * cmplx.Abs(v) / math.Hypot(xmax, ymax))
			return color.RGBA{r, g, b, 128}
		}
	}
	return color.Black
}
